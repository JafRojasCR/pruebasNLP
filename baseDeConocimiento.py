def quiz(materia):
    preguntas = materia["quiz"].get("preguntas", {})
    opciones = materia["quiz"].get("opciones", {})
    respuestas = materia["quiz"].get("respuestasCorrectas", {})

    if not preguntas:
        limpiar()
        input("No hay quiz disponible. ENTER para volver.")
        return

    letras = ["a", "b", "c", "d"]

    claves = list(preguntas.keys())  # todas las preguntas

    puntaje = 0

    for i in claves:
        limpiar()
        print("=== QUIZ ===\n")

        print(preguntas[i], "\n")

        for idx, op in enumerate(opciones[i]):
            print(f"{letras[idx]}) {op}")

        resp = input("\nRespuesta (a/b/c/d): ").lower()

        if resp in letras:
            indice = letras.index(resp)

            if indice == respuestas[i]:
                print("\nCorrecto!")
                puntaje += 1
            else:
                print("\nIncorrecto.")
                print("Respuesta correcta:", letras[respuestas[i]])
        else:
            print("\nRespuesta inválida.")

        input("\nPresione ENTER para continuar...")

    limpiar()
    print("=== RESULTADO FINAL ===")
    print(f"Puntaje: {puntaje}/{len(claves)}")

    input("\nENTER para volver al menú...")

