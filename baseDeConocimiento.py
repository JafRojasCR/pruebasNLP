def menu_materias(base):
    while True:
        limpiar()
        print("===== MENÚ DE MATERIAS =====")

        materias = list(base["materias"].keys())

        for i, m in enumerate(materias, 1):
            print(f"{i}. {m}")
        print("0. Salir")

        opcion = input("\nSeleccione una materia: ")

        if opcion == "0":
            limpiar()
            print("Programa terminado.")
            break

        if opcion.isdigit() and 1 <= int(opcion) <= len(materias):
            materia = materias[int(opcion) - 1]
            menu_materia(base["materias"][materia], materia)
        else:
            input("Opción inválida. Presione ENTER...")


